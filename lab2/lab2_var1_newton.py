import csv
import numpy as np
import matplotlib.pyplot as plt
import math


# ==============================================================================
# 1. Підготовка та зчитування даних (Варіант 1)
# ==============================================================================
def prepare_data():
    """
    Створює CSV-файл з експериментальними даними профілювання алгоритму
    (залежність часу від розміру вхідних даних n) та зчитує його [cite: 376-383].
    """
    filename = "data_var1.csv"
    # Експериментальні дані: розмір вибірки (n) та час виконання (t) у мс [cite: 382-383].
    data = [
        {"n": 1000, "t": 3},
        {"n": 2000, "t": 5},
        {"n": 4000, "t": 11},
        {"n": 8000, "t": 28},
        {"n": 16000, "t": 85}
    ]

    # Записуємо дані у файл CSV [cite: 353-358].
    with open(filename, 'w', newline='') as file:
        writer = csv.DictWriter(file, fieldnames=["n", "t"])
        writer.writeheader()
        writer.writerows(data)

    # Зчитуємо дані з файлу в масиви X та Y [cite: 359-375].
    x, y = [], []
    with open(filename, 'r', newline='') as file:
        reader = csv.DictReader(file)
        for row in reader:
            x.append(float(row['n']))
            y.append(float(row['t']))

    return np.array(x), np.array(y)


# ==============================================================================
# 2. Метод Ньютона через Розділені Різниці
# ==============================================================================
def divided_differences(x, y):
    """
    Обчислює таблицю розділених різниць[cite: 391].
    Розділена різниця k-го порядку обчислюється через різниці (k-1) порядку[cite: 198].
    Формула: f(x_i, ..., x_{i+k}) = [f(x_{i+1}, ..., x_{i+k}) - f(x_i, ..., x_{i+k-1})] / (x_{i+k} - x_i)[cite: 199].
    """
    n = len(y)
    coef = np.zeros([n, n])
    coef[:, 0] = y  # Розділена різниця нульового порядку співпадає зі значенням функції [cite: 194-195].

    for j in range(1, n):
        for i in range(n - j):
            # Обчислюємо за рекурентною формулою [cite: 198-199].
            coef[i][j] = (coef[i + 1][j - 1] - coef[i][j - 1]) / (x[i + j] - x[i])

    return coef


def newton_polynomial(x_data, y_data, x_target):
    """
    Обчислює значення інтерполяційного многочлена Ньютона в заданій точці x_target[cite: 386, 392].
    Формула: N_n(x) = f(x_0) + (x-x_0)f(x_0,x_1) + ... + (x-x_0)...(x-x_{n-1})f(x_0,...,x_n) [cite: 242-243].
    """
    coef = divided_differences(x_data, y_data)
    n = len(x_data)

    # Початкове значення N_n(x) = f(x_0) [cite: 243]
    result = coef[0, 0]

    # Додаємо наступні доданки ряду Ньютона [cite: 243, 345-347]
    for i in range(1, n):
        term = coef[0, i]  # Беремо верхній елемент з таблиці розділених різниць
        for j in range(i):
            # Множимо на (x - x_0)(x - x_1)...(x - x_{i-1}) [cite: 241, 344]
            term *= (x_target - x_data[j])
        result += term

    return result


# ==============================================================================
# 3. Факторіальні Многочлени та Скінченні Різниці
# ==============================================================================
def factorial_polynomial(x_data, y_data, x_target):
    """
    Прогнозування за допомогою факторіальних многочленів [cite: 392-393].
    Увага: скінченні різниці вимагають рівновіддалених вузлів [cite: 268-269].
    Наші x_data (1000, 2000, 4000...) є геометричною прогресією x_k = 1000 * 2^k.
    Щоб крок став рівним (h=1), ми робимо заміну змінної: t = log2(x / 1000).
    Тоді t приймає значення 0, 1, 2, 3, 4[cite: 270].
    """
    # Переходимо в нові координати t [cite: 269-270]
    t_target = np.log2(x_target / 1000)

    n = len(y_data)
    delta = np.zeros([n, n])
    delta[:, 0] = y_data

    # Будуємо таблицю скінченних різниць
    # Різницевий оператор: Δf(t) = f(t+1) - f(t) [cite: 272-273]
    for j in range(1, n):
        for i in range(n - j):
            delta[i][j] = delta[i + 1][j - 1] - delta[i][j - 1]

            # Обчислюємо функцію як ряд по факторіальних многочленах:
    # f(t) = сума [ (Δ^k f(0) / k!) * t^(k) ] [cite: 338]
    result = delta[0, 0]  # Початкове значення (k=0) [cite: 338]

    for k in range(1, n):
        # Факторіальний многочлен t^(k) = t(t-1)...(t-k+1) [cite: 322]
        t_fact = 1
        for m in range(k):
            t_fact *= (t_target - m)

        # Додаємо k-й член ряду [cite: 338]
        result += (delta[0, k] / math.factorial(k)) * t_fact

    return result


# ==============================================================================
# 4. Дослідницька частина: Аналіз ефекту Рунге [cite: 442, 491]
# ==============================================================================
def runge_function(x):
    """Еталонна функція y = 1 / (1 + 25x^2) для демонстрації ефекту Рунге."""
    return 1 / (1 + 25 * x ** 2)


def research_runge_effect():
    """
    Досліджує коливання полінома (ефект Рунге) при збільшенні
    кількості рівновіддалених вузлів: n = 5, 10, 20 [cite: 398-400, 489, 491].
    """
    x_dense = np.linspace(-1, 1, 500)  # Густа сітка для плавної побудови графіків
    y_true = runge_function(x_dense)  # Справжні значення функції

    nodes_list = [5, 10, 20]  # Кількість вузлів для дослідження [cite: 398-400]
    colors = ['red', 'green', 'blue']

    plt.figure(figsize=(12, 8))
    # Будуємо справжню функцію пунктиром
    plt.plot(x_dense, y_true, 'k--', label="Справжня функція Рунге f(x)", linewidth=2)

    for n_nodes, col in zip(nodes_list, colors):
        # Генеруємо n рівновіддалених вузлів на відрізку [-1, 1]
        x_nodes = np.linspace(-1, 1, n_nodes)
        y_nodes = runge_function(x_nodes)

        # Знаходимо значення інтерполяційного многочлена для кожного x_dense
        y_interp = [newton_polynomial(x_nodes, y_nodes, xi) for xi in x_dense]

        # Будуємо графік наближення та відмічаємо вузли
        plt.plot(x_dense, y_interp, color=col, label=f"Ньютон (n={n_nodes})", alpha=0.7)
        plt.scatter(x_nodes, y_nodes, color=col, s=40, zorder=5)

    plt.title("Дослідницька частина: Аналіз ефекту Рунге [cite: 491]")
    plt.xlabel("x")
    plt.ylabel("y")
    plt.ylim(-0.5, 1.5)  # Обмежуємо вісь Y, бо при n=20 коливання "відлітають" у нескінченність
    plt.legend()
    plt.grid(True)
    plt.show()


